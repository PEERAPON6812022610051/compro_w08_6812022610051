#include <stdio.h>

int main(){
    int i = 2;

    while (i <= 10){
        printf("%d ", i);
        i+=2;
    }
    printf("\nEnd of loop\n");

    return 0;
} 

// กำหนดตัวแปร i มีค่าเท่ากับ 2 แล้วเข้าสู่ลูป while ซึ่งจะทำงานไปเรื่อยๆ ตราบใดที่ i ยังไม่เกิน 10
// จากนั้นจะบวกค่า i เพิ่มทีละ 2 เพื่อจะให้ค่าเป็นเลขคู่ ตั้งเเต่ 2 ถึง 10 เมื่อ i มีค่ามากกว่า 10 เเล้ว ลูปจะหยุดทำงานและแสดงข้อความ